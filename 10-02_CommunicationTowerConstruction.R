### ------------------VARIABLE DEFINITION---------------------

## In a town of 5x5 grid, with a given annual revenue per area, r, and 
## cost per built communication tower, c, we try to optimise coverage
## as well as profit, giving the two a weighting each.

# As shown in script, we begin by finding maximum profit, and the 
# maximum coverage. These values we set as C and P.

# x is to be a binary variable saying whether cell i is covered or not.
# They are covered if a communication tower is built on the cell, or 
# North, South, West or East of it.
# y is a binary variable saying whether cell i has a communication 
# tower built on it.
# z is an integer counting how often the cell i is covered.

### ---------------VARIABLE INPUT-------------------

# We define the objective function, Z, as the following.
#   Z = w.a*(SUM(x)-C)/C + w.b*(((SUM(x*r)-SUM£(y*c))-P))/P

# The equation makes up the relative difference between the coverage of
# the town and the profit of the firm. These parts are weighted using 
# w.a and w.b

# We re-write the equations to simplify for x and y. Also included in 
# the objective function is z, which does not take anx value, so that 
# the optimisation does not take the value of z into account. In the 
# final equation we also include a constant which is a result of the 
# re-writing and use the constraints

# The new objective function after being rewritten looks something 
# like this:
#   Z = SUM((-(w.a/C + w.b*r[i]/P)) * x[i]) + 
#       SUM((w.b*c[1]/P) * y[i]) + (w.a+w.b)

# For the weights:
w.a <- 1 # Maximising coverage is given an importance of 1
w.b <- 3 # Making a profit is given an importance of 1, for part B: 3

